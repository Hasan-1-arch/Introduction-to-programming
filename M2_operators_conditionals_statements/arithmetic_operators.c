#include<stdio.h>
int main(){
    int a = 10, b = 5;
    
    int sum = a + b;
    printf("sum = %d\n", sum);

    int subs = a - b;
    printf("subs = %d\n", subs);

    int mul = a * b;
    printf("mul = %d\n", mul);

    int div = a / b;
    printf("div = %d\n", div);

    int rem = 12 % 5;
    printf("rem = %d\n", rem);
    
    return 0;
}